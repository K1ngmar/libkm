long	km_atol(const char *str)
{
	long long res = 0;
	long long sgn = 1;

	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '-')
		sgn = -1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str >= '0' && *str <= '9') {
		res = res * 10 + *str - '0';
		str++;
	}
	return ((res * sgn));
}

int		km_atoi(const char *str)
{
	return ((int)km_atol(str));
}
